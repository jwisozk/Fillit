#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	ssize_t	nn;

	nn = nb;
	if (nn < 0)
	{
		ft_putchar_fd('-', fd);
		nn = -nn;
	}
	if (nn >= 10)
		ft_putnbr_fd(nn / 10, fd);
	ft_putchar_fd('0' + nn % 10, fd);
}
